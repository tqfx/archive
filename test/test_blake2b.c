static void test_blake2b_160(void)
{
    static const struct
    {
        const char *msg;
        unsigned char hash[A_BLAKE2B_160_DIGESTSIZE];
    } tests[] = {
        /* clang-format off */
        {
            "",
            {
                0x33, 0x45, 0x52, 0x4A, 0xBF, 0x6B, 0xBE, 0x18,
                0x09, 0x44, 0x92, 0x24, 0xB5, 0x97, 0x2C, 0x41,
                0x79, 0x0B, 0x6C, 0xF2,
            },
        },
        { "abc",
            {
                0x38, 0x42, 0x64, 0xF6, 0x76, 0xF3, 0x95, 0x36,
                0x84, 0x05, 0x23, 0xF2, 0x84, 0x92, 0x1C, 0xDC,
                0x68, 0xB6, 0x84, 0x6B,
            },
        },
        /* clang-format on */
    };

    a_blake2b_t ctx[1];

    for (unsigned int i = 0; i != sizeof(tests) / sizeof(*tests); ++i)
    {
        a_blake2b_160(tests[i].msg, strlen(tests[i].msg), ctx->out);
        __HASH_DIFF(ctx->out, tests[i].hash, A_BLAKE2B_160_DIGESTSIZE);
    }
}

